//  .: Operadores Aritiméticos :.

// multiplicação *
console.log(3.2 * 8.5)

// divisão /
console.log(8.2 / 3.5)

// soma +
console.log(8.2 + 3.5)

// subtração -
console.log(8.2 - 3.5)

// resto de divisão %
let remainder
remainder = 11 % 9
console.log(remainder)

// incremento ++
let increment = 0
// adicina o encremento depois da execução
console.log(increment++)
// adicina o encremento antes da execução
console.log(++increment)

// decremento --
let decrement = 0
// adicina o decremento depois da execução
console.log(decrement--)
// adicina o decremento antes da execução
console.log(--decrement)

// exponencial **
console.log(2 ** 3)

// Grouping operator ( )
let total = (2 + 3) * 5
console.log(total)