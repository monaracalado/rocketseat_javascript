//   .: Operadores de comparação :.

// Irá comparar valores e retornar um Boolean como resposta à comparação

let one = 1
let two = 2

// ==  lê-se igual a
console.log( two == 1)
console.log( one == '1')

// !=  lê-se diferente de
console.log( one != two)
console.log( one != 1)
console.log( one != '1')

// ===  lê-se estritamente igual a 
console.log( two === '2')
console.log( two === 2)

// !==  lê-se estritamente diferente de 
console.log( one !== '1')
console.log( one !== 1)

// >  lê-se maior que
console.log(one > two)

// >=  lê-se maior igual a
console.log(one >= 1)
console.log(two >= 1)

// <  lê-se menor que
console.log(one < two)

// <=  lê-se menor igual a
console.log(one <= two)
console.log(one <= 1)
console.log(one <= 0)
